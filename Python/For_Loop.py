# For Loop with range() in python
# range>> start>> condition>> increment by defolt -1
for n in range (22):
    print(n)
for n in range(1,6):
    print(n)
for n in range(1,6,2):
    print(n)
    
for a in range(1,11):
    print("2*",a,"=",2*a)

# in case of reverse
for n in range (10,0,-1):
    print(n)
    